/**
 * Build SCSS and Tailwind outputs from layered token JSON sources.
 *
 * Source of truth:
 *   tokens/core/primitives.json      — shared console/docs palette
 *   tokens/console/extensions.json   — console-only (e.g. aqua)
 *   tokens/marketing/extensions.json — marketing-only (salmon, fuchsia, legacy gray)
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const GENERATED = path.join(ROOT, "generated");

const HEADER = `// Copyright 2026, Pulumi Corporation. All rights reserved.
// GENERATED BY scripts/build-tokens.mjs — DO NOT EDIT BY HAND.
`;

function readJson(relativePath) {
    return JSON.parse(fs.readFileSync(path.join(ROOT, relativePath), "utf8"));
}

function mergeConsolePalettes() {
    const core = readJson("tokens/core/primitives.json");
    const consoleExt = readJson("tokens/console/extensions.json");
    return {
        white: core.white,
        black: core.black,
        palettes: { ...core.palettes, ...consoleExt.palettes },
        product: "console",
    };
}

function readMarketingTokens() {
    return readJson("tokens/marketing/extensions.json");
}

function resolveBrandRefs(value, brand) {
    if (typeof value !== "string") return value;
    const match = value.match(/^\{brand\.([a-z-]+)\}$/);
    if (!match) return value;
    const key = match[1];
    if (!(key in brand)) {
        throw new Error(`Unknown brand reference: ${value}`);
    }
    return brand[key];
}

function resolvePalette(palette, brand) {
    const resolved = {};
    for (const [shade, value] of Object.entries(palette)) {
        resolved[shade] = resolveBrandRefs(value, brand);
    }
    return resolved;
}

/** @param {Record<string, Record<string, string>>} palettes */
function scssPaletteMap(family, palette) {
    const lines = Object.entries(palette)
        .sort(([a], [b]) => Number(a) - Number(b))
        .map(([shade, hex]) => `    ${shade}: ${hex},`);
    return `$${family}: (\n${lines.join("\n")}\n);`;
}

function scssBrandMap(brand) {
    const lines = Object.entries(brand).map(([key, hex]) => `    "${key}": ${hex},`);
    return `$brand: (\n${lines.join("\n")}\n);`;
}

function generateConsolePrimitivePalettesScss(consoleTokens) {
    const families = Object.keys(consoleTokens.palettes).sort();
    const maps = families.map((f) => scssPaletteMap(f, consoleTokens.palettes[f]));

    const colorsEntries = families.map((f) => `    "${f}": $${f},`).join("\n");

    return `${HEADER}
@use "sass:map";

$white: ${consoleTokens.white};
$black: ${consoleTokens.black};

${maps.join("\n\n")}

$colors: (
${colorsEntries}
);
$single-colors: (
    "white": $white,
    "black": $black,
);
$color-shades: 100, 200, 300, 400, 500, 600, 700, 800, 900, 1000;
`;
}

