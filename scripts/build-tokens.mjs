function generateMarketingWwwColorsScss(marketing) {
    const brand = marketing.brand;
    const resolvedPalettes = {};
    for (const [family, palette] of Object.entries(marketing.palettes)) {
        resolvedPalettes[family] = resolvePalette(palette, brand);
    }

    const brandAliases = [
        "yellow",
        "salmon",
        "fuchsia",
        "purple",
        "violet",
        "blue",
    ]
        .map((key) => `$brand-${key}: map.get($brand, "${key}");`)
        .join("\n");

    const paletteMaps = Object.entries(resolvedPalettes)
        .map(([family, palette]) => scssPaletteMap(family, palette))
        .join("\n\n");

    return `${HEADER}
@use "sass:map";

$black: #000;
$white: #fff;

${scssBrandMap(brand)}

${brandAliases}

${paletteMaps}
`;
}

function generateLegacyWwwJson(marketing) {
    const meta = readJson("tokens/marketing/meta.json");
    const brand = {};
    for (const [key, hex] of Object.entries(marketing.brand)) {
        brand[key] = { value: hex, type: "color" };
    }

    const colors = { brand: {} };
    for (const [key, entry] of Object.entries(brand)) {
        colors.brand[key] = entry;
    }

    for (const [family, palette] of Object.entries(marketing.palettes)) {
        colors[family] = {};
        const resolved = resolvePalette(palette, marketing.brand);
        for (const [shade, hex] of Object.entries(resolved)) {
            colors[family][shade] = { value: hex, type: "color" };
        }
    }

    // Tailwind v2 legacy gray includes 850 (maps to marketing gray-900 / old www gray-850)
    if (colors.gray?.["900"]) {
        colors.gray["850"] = { value: colors.gray["900"].value, type: "color" };
    }

    return {
        global: {
            colors,
            typography: meta.typography,
            shape: meta.shape,
        },
    };
}

function generateTokenManifest(consoleTokens, marketing) {
    const coreFamilies = new Set(Object.keys(readJson("tokens/core/primitives.json").palettes));
    const consoleOnlyFamilies = new Set(Object.keys(readJson("tokens/console/extensions.json").palettes));
    const marketingFamilies = new Set(Object.keys(marketing.palettes));

    const sharedFamilies = [...coreFamilies].filter((f) => !consoleOnlyFamilies.has(f));
    const marketingOnlyFamilies = [...marketingFamilies].filter(
        (f) => !coreFamilies.has(f) || f === "salmon" || f === "fuchsia" || f === "purple",
    );

    const conflicts = [];
    for (const family of coreFamilies) {
        if (!marketing.palettes[family]) continue;
        const corePalette = consoleTokens.palettes[family];
        const mktPalette = resolvePalette(marketing.palettes[family], marketing.brand);
        for (const shade of Object.keys(corePalette)) {
            if (shade in mktPalette && corePalette[shade].toLowerCase() !== mktPalette[shade].toLowerCase()) {
                conflicts.push({
                    family,
                    shade,
                    core: corePalette[shade],
                    marketing: mktPalette[shade],
                });
            }
        }
    }

    return {
        layers: {
            core: {
                path: "tokens/core/primitives.json",
                families: [...coreFamilies],
            },
            console: {
                path: "tokens/console/extensions.json",
                families: [...consoleOnlyFamilies],
            },
            marketing: {
                path: "tokens/marketing/extensions.json",
                families: [...marketingFamilies],
                brandKeys: Object.keys(marketing.brand),
            },
        },
        summary: {
            coreFamilyCount: coreFamilies.size,
            consoleOnlyFamilyCount: consoleOnlyFamilies.size,
            marketingFamilyCount: marketingFamilies.size,
            sharedFamilyNames: sharedFamilies,
            marketingOnlyFamilyNames: ["salmon", "fuchsia", "purple", "brand"],
            overlappingFamiliesWithDifferentValues: conflicts,
        },
    };
}

function writeGenerated(relativePath, content) {
    const fullPath = path.join(GENERATED, relativePath);
    fs.mkdirSync(path.dirname(fullPath), { recursive: true });
    fs.writeFileSync(fullPath, content, "utf8");
    console.log(`Wrote generated/${relativePath}`);
}

function main() {
    const consoleTokens = mergeConsolePalettes();
    const marketing = readMarketingTokens();

    writeGenerated("console/_primitive-palettes.scss", generateConsolePrimitivePalettesScss(consoleTokens));
    writeGenerated("console/tailwind-v4/_theme.scss", generateConsoleTailwindV4ThemeScss(consoleTokens));
    writeGenerated("marketing/_www-colors.scss", generateMarketingWwwColorsScss(marketing));
    writeGenerated("token-manifest.json", `${JSON.stringify(generateTokenManifest(consoleTokens, marketing), null, 4)}\n`);

    const wwwJsonPath = path.join(ROOT, "tokens/www.json");
    fs.writeFileSync(wwwJsonPath, `${JSON.stringify(generateLegacyWwwJson(marketing), null, 4)}\n`, "utf8");
    console.log("Wrote tokens/www.json (legacy Tailwind v2 compat, generated)");

    const conflicts = generateTokenManifest(consoleTokens, marketing).summary.overlappingFamiliesWithDifferentValues;
    if (conflicts.length > 0) {
        console.log(`\nToken manifest: ${conflicts.length} core/marketing palette conflicts (expected for legacy www):`);
        for (const c of conflicts.slice(0, 5)) {
            console.log(`  ${c.family}-${c.shade}: core=${c.core} marketing=${c.marketing}`);
        }
        if (conflicts.length > 5) console.log(`  ... and ${conflicts.length - 5} more`);
    }
}

main();
